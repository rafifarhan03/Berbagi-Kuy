    private void TabelDonaturMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelDonaturMouseClicked
        // TODO add your handling code here:
        int baris= TabelDonatur.rowAtPoint(evt.getPoint());
        String nama= TabelDonatur.getValueAt(baris, 0).toString();
        NamaField.setText(nama);
        String Jk= TabelDonatur.getValueAt(baris, 1).toString();
        JKCombo.setSelectedItem(Jk);
        String telp= TabelDonatur.getValueAt(baris, 2).toString();
        TelponField.setText(telp);
        String email= TabelDonatur.getValueAt(baris, 3).toString();
        EmailField.setText(email);
        String rek= TabelDonatur.getValueAt(baris, 4).toString();
        RekField.setText(rek);
        String jlh= TabelDonatur.getValueAt(baris, 5).toString();
        JlhDonasiField.setText(jlh);
        String tujuan= TabelDonatur.getValueAt(baris, 6).toString();
        TujuanCombo.setSelectedItem(tujuan);
        String jenis= TabelDonatur.getValueAt(baris, 7).toString();
        JenisCombo.setSelectedItem(jenis);
    }//GEN-LAST:event_TabelDonaturMouseClicked

