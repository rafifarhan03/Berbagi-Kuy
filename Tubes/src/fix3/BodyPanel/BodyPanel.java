    private void tampilkan_data(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nama ");
        model.addColumn("Jenis Kelamin");
        model.addColumn("No Telpon");
        model.addColumn("Email");
        model.addColumn("Nomor Rekening");
        model.addColumn("Jumlah Donasi");
        model.addColumn("Tujuan Berbagi");
        model.addColumn("Jenis Pembayaran");
        try{
            String sql ="Select * from data_donatur";
            java.sql.Connection conn = (Connection)Konfig.configDB();
            java.sql.Statement stm= conn.createStatement();
            java.sql.ResultSet res= stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),res.getString(7),res.getString(8)});
            }
            TabelDonatur.setModel(model);
            
        }catch (SQLException e){
            System.out.println("Error : "+e.getMessage());
        }
    }
